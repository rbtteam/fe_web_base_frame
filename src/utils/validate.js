/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path);
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validUsername(str) {
  const phoneReg = /^(?:(?:\+|00)86)?1\d{10}$/;
  return phoneReg.test(str);
}
